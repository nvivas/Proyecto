    private void guardar() throws Exception {

        String nombreColor;
        int id, largo, ancho, grueso, idProvee;

        nombreColor = txtColor.getText();

        id = Integer.parseInt(txtId.getText());
        largo = Integer.parseInt(txtLargo.getText());
        ancho = Integer.parseInt(txtAncho.getText());
        grueso = Integer.parseInt(txtGrueso.getText());
        idProvee = Integer.parseInt(txtProveedor.getText());

        tarea.setId_color(id);
        tarea.setColor(nombreColor);
        tarea.setLargo(largo);
        tarea.setAncho(ancho);
        tarea.setGrueso(grueso);

        int decision = JOptionPane.showConfirmDialog(null, "¬øEst√° seguro/a que desea modificar este Color?", "Advertencia", JOptionPane.YES_NO_OPTION);
        if (decision == 0) {
            try {
                tareas_color.modificarColor(tarea);
                ModificarColor.this.dispose();
                PpalColor vista = new PpalColor();
                vista.setVisible(true);
                vista.setLocationRelativeTo(null);

            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
                JOptionPane.showMessageDialog(this, "Ha surgido un error y no se ha podido eliminar el registro.");
            }
        }
    }

